    private void btnActActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActActionPerformed
        try{
            DriverManager.registerDriver(new org.gjt.mm.mysql.Driver());
            Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/Productos","root","");
            Statement s = conexion.createStatement();
            String SQL = "UPDATE `Producto` SET  `DESCRIPCION`='%des', `PRECIO`='%pre', `EXISTENCIA`='%exi' WHERE `IDPRODUCTOS` = "+id;
            SQL = SQL.replace("%des",txtdes.getText());
            SQL = SQL.replace("%pre",txtpre.getText());
            SQL = SQL.replace("%exi",txtexi.getText());
            int respuesta = s.executeUpdate(SQL);
            String texto ="ERROR NO SE PUDO ACTUALIZAR";
            if(respuesta!=0){
                texto = "SE ACTUALIZO CORRECTAMENTE";
            }else{
                mensaje(texto);
            }
            mensaje(texto);
            txtpre.setText("");
            txtexi.setText("");
            txtdes.setText("");
            conexion.close();
        }catch(Exception e){
            mensaje(e.getMessage());
        }
    }//GEN-LAST:event_btnActActionPerformed

