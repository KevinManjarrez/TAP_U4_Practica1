     private void obtener(String id){
        try{
            DriverManager.registerDriver(new org.gjt.mm.mysql.Driver());
            Connection conexion = DriverManager.getConnection("jdbc:mysql://localhost/Productos","root","");
            Statement s = conexion.createStatement();
            String SQL ="SELECT * FROM `Producto` WHERE IDPRODUCTOS ="+id;
            ResultSet resultado = s.executeQuery(SQL);
            if(resultado.next()){
               txtdes.setText(resultado.getString(2));
               txtpre.setText(resultado.getString(3));
               txtexi.setText(resultado.getString(4));
            }else{
                mensaje("Se borro con éxito el "+id);
            }
            conexion.close();
        }catch(Exception e){
            mensaje(e.getMessage());
        }       
     }
     
